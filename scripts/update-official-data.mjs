function parseMarkdownPriceRow(text, modelName) {
  const escaped = modelName.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  const row = text.match(new RegExp(`\\|\\s*${escaped}\\s*\\|\\s*\\\\?\\$([0-9.]+)\\s*\\|\\s*\\\\?\\$([0-9.]+)\\s*\\|[^|]*\\|\\s*\\\\?\\$([0-9.]+)`, "i"));
  if (!row) return null;
  return {
    input: Number(row[1]),
    cache: Number(row[2]),
    output: Number(row[3])
  };
}

function extractPrices(text) {
  const candidates = [];
  const regexes = [
    new RegExp(`¥\\s*${moneyPattern}`, "g"),
    new RegExp(`￥\\s*${moneyPattern}`, "g"),
    new RegExp(`(?:CNY|RMB)\\s*${moneyPattern}`, "gi"),
    new RegExp(`${moneyPattern}\\s*元`, "g")
  ];

  for (const regex of regexes) {
    for (const match of text.matchAll(regex)) {
      const raw = Number(match[1]);
      if (Number.isFinite(raw) && raw >= 0 && raw < 1000) candidates.push(raw);
    }
    if (candidates.length >= 3) break;
  }

  return [...new Set(candidates)].slice(0, 6);
}

function renderUpdateReport(data, previous = {}) {
  const lines = [
    "# Official Price Update Report",
    "",
    `Generated at: ${data.generatedAt}`,
    "",
    "## Summary",
    "",
    `- Sources checked: ${data.sources.length}`,
    `- Parsed API models: ${data.models.filter((item) => !item.carriedForward).length}`,
    `- Parsed plans: ${data.plans.filter((item) => !item.carriedForward).length}`,
    `- Review items: ${data.reviewQueue.length}`,
    `- Fetch failures: ${data.reviewQueue.filter((item) => item.type === "fetch-failed").length}`,
    "",
    "## Parsed Plans",
    ""
  ];

  const parsedPlans = data.plans.filter((plan) => !plan.carriedForward);
  if (parsedPlans.length) {
    parsedPlans.forEach((plan) => {
      lines.push(`- ${plan.providerId} / ${plan.name}: ${plan.firstMonth ? `首月 ${plan.firstMonth}; ` : ""}${plan.recurringMonthly || "-"}; ${plan.quota || plan.tokenLimit || "-"}`);
    });
  } else {
    lines.push("- None");
  }

  const changes = diffEntities("models", data.models, previous.models).concat(diffEntities("plans", data.plans, previous.plans));
  lines.push("", "## Changes", "");
  if (changes.length) {
    changes.forEach((change) => lines.push(`- ${change}`));
  } else {
    lines.push("- No structured model/plan field changes detected.");
  }

  lines.push("", "## Review Queue", "");
  if (data.reviewQueue.length) {
    data.reviewQueue.forEach((item) => {
      lines.push(`- [${item.severity}] ${item.providerName || item.providerId} / ${item.type}: ${item.message} (${item.sourceUrl})`);
    });
  } else {
    lines.push("- Empty");
  }

  lines.push("", "## Source Health", "");
  data.sources.forEach((source) => {
    const status = source.httpStatus ? `HTTP ${source.httpStatus}` : "fetch failed";
    lines.push(`- ${source.providerName} / ${source.type}: ${status}, ${source.contentLength || 0} chars, parser=${source.parser || "none"}`);
  });

  return `${lines.join("\n")}\n`;
}

function diffEntities(kind, current = [], previous = []) {
  const previousById = new Map(previous.map((item) => [item.id, item]));
  const fields = kind === "models"
    ? ["name", "currency", "unit", "context", "maxOutput", "cacheHitInput", "cacheMissInput", "output", "priceText"]
    : ["name", "planType", "firstMonth", "recurringMonthly", "quarterly", "yearly", "request5h", "weeklyRequests", "monthlyRequests", "tokenLimit", "quota"];
  const changes = [];

  current.forEach((item) => {
    const before = previousById.get(item.id);
    if (!before) {
      changes.push(`${kind}: added ${item.id}`);
      return;
    }
    const changedFields = fields.filter((field) => JSON.stringify(before[field] ?? null) !== JSON.stringify(item[field] ?? null));
    if (changedFields.length) {
      changes.push(`${kind}: ${item.id} changed ${changedFields.join(", ")}`);
    }
  });

  return changes;
}

