import { createHash } from "node:crypto";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const sourcesPath = resolve(root, "data/vendor-sources.json");
const outputPath = resolve(root, "data/official-price-data.json");
const reportPath = resolve(root, "data/update-report.md");

const now = new Date();
const generatedAt = now.toISOString();
const verifiedAt = generatedAt.slice(0, 10);

const moneyPattern = "([0-9]+(?:\\.[0-9]+)?)";

async function main() {
  const registry = JSON.parse(await readFile(sourcesPath, "utf8"));
  const previous = await readJsonIfExists(outputPath);
  const models = [];
  const plans = [];
  const sourceResults = [];
  const reviewQueue = [];

  for (const source of registry.sources) {
    const result = await fetchSource(source);
    sourceResults.push(result.summary);

    if (!result.ok) {
      reviewQueue.push({
        providerId: source.providerId,
        providerName: source.providerName,
        type: "fetch-failed",
        severity: "high",
        message: `无法抓取官方来源：${result.error}`,
        sourceUrl: source.url,
        detectedAt: generatedAt
      });
      continue;
    }

    const previousSource = previous.sources?.find((entry) => entry.url === source.url);
    if (previousSource && previousSource.sha256 !== result.summary.sha256) {
      reviewQueue.push({
        providerId: source.providerId,
        providerName: source.providerName,
        type: "source-changed",
        severity: source.parser ? "medium" : "high",
        message: source.parser
          ? "官方来源内容发生变化，已尝试结构化解析，请人工抽查。"
          : "官方来源内容发生变化，但尚未实现结构化解析器，请人工复核价格表。",
        sourceUrl: source.url,
        previousSha256: previousSource.sha256,
        currentSha256: result.summary.sha256,
        detectedAt: generatedAt
      });
    }

    const parsed = parseSource(source, result.text, result.rawText);
    if (parsed) {
      models.push(...(parsed.models || []));
      plans.push(...(parsed.plans || []));
      reviewQueue.push(...(parsed.reviewQueue || []));
    } else {
      reviewQueue.push({
        providerId: source.providerId,
        providerName: source.providerName,
        type: "parser-missing",
        severity: "medium",
        message: "已抓取官方页面快照，但还没有对应解析器；该厂商价格不得自动发布。",
        sourceUrl: source.url,
        detectedAt: generatedAt
      });
    }
  }

  const data = {
    generatedAt,
    note: "Generated by scripts/update-official-data.mjs. Only parser-backed official values should be published automatically; all ambiguous values stay in reviewQueue.",
    models: mergeWithPreviousModels(models, previous.models),
    plans: mergeWithPreviousPlans(plans, previous.plans),
    sources: sourceResults,
    reviewQueue
  };

  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, `${JSON.stringify(data, null, 2)}\n`);
  await writeFile(reportPath, renderUpdateReport(data, previous));

  console.log(`Updated ${outputPath}`);
  console.log(`Updated ${reportPath}`);
  console.log(`Parsed models: ${models.length}`);
  console.log(`Parsed plans: ${plans.length}`);
  console.log(`Review items: ${reviewQueue.length}`);
}

function parseSource(source, text, rawText = text) {
  const parsers = {
    deepseekPricing: parseDeepSeekPricing,
    mimoPricing: parseMiMoPricing,
    mimoTokenPlan: parseMiMoTokenPlan,
    aliyunTokenPlan: parseAliyunTokenPlan,
    jdcloudCodingPlan: parseJdcloudCodingPlan,
    xfyunCodingPlan: parseXfyunCodingPlan,
    baiduCodingPlan: parseBaiduCodingPlan
  };
  const parser = parsers[source.parser];
  return parser ? parser(text, source.url, rawText) : null;
}

function mergeWithPreviousPlans(plans, previousPlans = []) {
  const merged = new Map();
  previousPlans.forEach((plan) => {
    if (plan?.id && plan.status === "official") {
      merged.set(plan.id, {
        ...plan,
        carriedForward: true,
        carryForwardReason: "Latest update did not parse a replacement from the official source."
      });
    }
  });
  plans.forEach((plan) => {
    if (plan?.id) merged.set(plan.id, { ...plan, carriedForward: false });
  });
  return [...merged.values()];
}

function mergeWithPreviousModels(models, previousModels = []) {
  const merged = new Map();
  const skipCarryForwardIds = new Set(["qwen-plus", "glm-4-plus", "kimi-k2", "minimax-text-01", "qwen3-max-pricing"]);
  previousModels.forEach((model) => {
    if (skipCarryForwardIds.has(model?.id)) return;
    if (model?.id && (model.status === "official" || model.status === "reviewed")) {
      merged.set(model.id, {
        ...model,
        carriedForward: true,
        carryForwardReason: "Latest update did not parse a replacement from the official source."
      });
    }
  });
  models.forEach((model) => {
    if (model?.id) merged.set(model.id, { ...model, carriedForward: false });
  });
  return [...merged.values()];
}

async function fetchSource(source) {
  const controller = new AbortController();
  const timeout = setTimeout(() => controller.abort(), 25000);
  try {
    const response = await fetch(source.url, {
      signal: controller.signal,
      headers: {
        "user-agent": "api-price-monitor/0.1 (+https://example.local)",
        accept: "text/html,application/xhtml+xml,application/json,text/plain;q=0.9,*/*;q=0.8"
      }
    });
    const text = await response.text();
    const normalizedText = normalizeText(text);
    return {
      ok: response.ok,
      text: normalizedText,
      rawText: text,
      error: response.ok ? null : `HTTP ${response.status}`,
      summary: {
        providerId: source.providerId,
        providerName: source.providerName,
        type: source.type,
        url: source.url,
        parser: source.parser,
        publishMode: source.publishMode,
        codePlanAvailability: source.codePlanAvailability,
        httpStatus: response.status,
        fetchedAt: generatedAt,
        sha256: sha256(normalizedText),
        contentLength: normalizedText.length
      }
    };
  } catch (error) {
    return {
      ok: false,
      text: "",
      rawText: "",
      error: error.name === "AbortError" ? "timeout" : error.message,
      summary: {
        providerId: source.providerId,
        providerName: source.providerName,
        type: source.type,
        url: source.url,
        parser: source.parser,
        publishMode: source.publishMode,
        codePlanAvailability: source.codePlanAvailability,
        httpStatus: null,
        fetchedAt: generatedAt,
        sha256: null,
        contentLength: 0
      }
    };
  } finally {
    clearTimeout(timeout);
  }
}

